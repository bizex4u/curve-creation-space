<h2>FAQs about barter advertising in India</h2>
<h3>Is barter advertising legal in India?</h3>
<p>Yes. Barter is a recognised commercial transaction under Indian contract law. Both parties must raise tax invoices for the value exchanged and GST is applicable on both legs of the transaction.</p>

<h3>Does GST apply to barter advertising?</h3>
<p>Yes. Under the CGST Act, barter is treated as a supply of goods or services. Both the brand and the media owner raise invoices at the agreed value and pay GST on their respective legs.</p>

<h3>What is the minimum deal size?</h3>
<p>Most agencies, including BIZEX4U, work on barter deals starting at around ₹5 lakh in media value. Below that, the cost of paperwork and fulfilment usually outweighs the benefit.</p>

<h3>Can I do part-barter, part-cash?</h3>
<p>Yes — and it is the most common structure. A typical mix is 60–70% barter and 30–40% cash, especially for outdoor and print media.</p>

