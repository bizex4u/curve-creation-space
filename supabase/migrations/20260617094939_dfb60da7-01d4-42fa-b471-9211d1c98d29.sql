<h2>FAQs: ATL vs BTL advertising</h2>
<h3>Is digital advertising ATL or BTL?</h3>
<p>It depends on the format. YouTube masthead, connected TV and programmatic display targeting broad demographics are usually counted as ATL. Search ads, retargeting, WhatsApp messaging and CRM are BTL.</p>

<h3>Which is cheaper — ATL or BTL?</h3>
<p>BTL has lower upfront media cost but higher manpower cost per impression. ATL costs more in absolute media spend but is cheaper per impression at scale.</p>

<h3>Can a small Indian brand only do BTL?</h3>
<p>Yes, especially in the first 6–12 months. Hyperlocal BTL (RWA, modern trade, school contact) often delivers better ROI than ATL for early-stage brands.</p>

